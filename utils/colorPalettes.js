/**
 * Get color palette for a specific theme
 * @param {string} backgroundColor - The background color theme name
 * @returns {Object} Color palette object
 */
function getColorPalette(backgroundColor = 'black') {
  return colorPalettes[backgroundColor] || colorPalettes.black;
}

/**
 * Get email-specific colors based on theme
 * @param {Object} theme - Theme object from database
 * @returns {Object} Email color scheme
 */
function getEmailColors(theme) {
  const palette = getColorPalette(theme.backgroundColor);

  return {
    primary: theme.primaryColor || palette.primary,
    secondary: theme.secondaryColor || palette.secondary,
    background: palette.background,
    header: palette.navbar,
    headerText: theme.primaryColor || palette.primary,
    content: '#f9f9f9',
    button: theme.primaryColor || palette.primary,
    buttonText: '#FFFFFF',
    footer: palette.accordionContentLight,
    border: palette.secondaryNav,
  };
}

