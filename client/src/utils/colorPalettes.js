// Helper function to get color palette
export const getColorPalette = theme => {
  return colorPalettes[theme] || colorPalettes.black;
};

// Helper function to get specific color
export const getColor = (theme, colorType) => {
  const palette = getColorPalette(theme);
  return palette[colorType] || palette.primary;
};

// Helper function to get preselected colors
export const getPreselectedColors = type => {
  return preselectedColors[type] || preselectedColors.primary;
};

// Helper function to find color name by value
export const getColorName = (type, value) => {
  const colors = getPreselectedColors(type);
  const color = colors.find(c => c.value === value);
  return color ? color.name : 'Custom';
};

